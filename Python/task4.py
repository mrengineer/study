print("# Дано несколько чисел. Вычислите их сумму. Сначала вводите количество чисел N, затем вводится ровно N целых чисел.")
N = int(input("Введите количество чисел: "))
s = 0
for i in range(N):
    num = int(input("Введите число: "))
    s += num
print("Сумма чисел:", s)



print("# По данному натуральному n вычислите сумму 1**3+2**3+3**3+...+n**3.")
n = int(input("Введите натуральное число n: "))
s = 0
for i in range(1, n + 1):
    s += i ** 3
print("Сумма кубов:", s)



print("# По данному натуральному n вычислите значение n!")
n = int(input("Введите натуральное число n: "))
fact = 1
for i in range(1, n + 1):
    fact *= i
print("Факториал:", fact)



print("# Подсчитайте количество нулей среди введенных чисел.")
N = int(input("Введите количество чисел: "))
c = 0
for i in range(N):
    num = int(input("Введите число: "))
    if num == 0:
        c += 1
print("Количество нулей:", c)



print("# Подсчитайте количество четных и нечетных цифр числа.")
n = int(input("Введите число: "))
even_count = 0
odd_count = 0
for digit in str(n):
    if int(digit) % 2 == 0:
        even_count += 1
    else:
        odd_count += 1
print(f"Четных: {even_count}, Нечетных: {odd_count}")



print("# Выведите сумму цифр текущего числа.")
n = int(input("Введите число: "))
s = 0
for digit in str(n):
    s += int(digit)
print("Сумма цифр:", s)



print("# Выведите единственное число - количество делителей числа x.")
x = int(input("Введите число x: "))
c = 0
for i in range(1, x + 1):
    if x % i == 0:
        c += 1
print("Количество делителей:", c)